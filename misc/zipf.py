def ngrams(seq, n):
    return [seq[i:i+n] for i in range(len(seq)-n+1)]

def zipf(ngrams):
    uniq = set(ngrams)
    occs = [ngrams.count(x) for x in uniq]
    return [x * i for i, x in enumerate(sorted(occs, reverse=True))]

